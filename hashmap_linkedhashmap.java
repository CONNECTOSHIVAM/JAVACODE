import java.util.*;

public class hashmap_linkedhashmap {
    
    public static void main(String[] args) {
        
        //HashMap program
        HashMap hm = new HashMap();
        hm.put(1,"shivam");
        hm.put(5,"Sans");
        hm.put(2,"kriti");
        System.out.println(hm);

        //LinkedHashMap program
        LinkedHashMap lh = new LinkedHashMap();
        lh.put(7, "msd");
        lh.put(9,"sk");
        lh.put(2,"lalu");
        System.out.println(lh);
    }
}
